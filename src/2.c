#include <stdio.h>
#include <math.h>

int main() {
    int a = 5;
    int b = 10;
    int c = 2;
    int d = 3;
    int e = 4;
    int f = 7;
    int g = 11;
    int h = 9;
    int i = 6;
    int j = 8;

    printf("The sum of %d and %d is %d\n", a, b, a + b);
    printf("The difference between %d and %d is %d\n", c, d, c - d);
    printf("The product of %d and %d is %d\n", e, f, e * f);
    printf("The quotient of %d and %d is %d\n", g, h, g / h);
    printf("The remainder when %d is divided by %d is %d\n", i, j, i % j);

    return 0;
}
